export function BottomNav() {
  const pathname = usePathname();

  return (
    <nav
      className="fixed bottom-0 left-0 right-0 z-40 glass"
      style={{ paddingBottom: "var(--safe-bottom)" }}
    >
      <div className="flex items-center justify-around h-[var(--nav-height)] max-w-lg mx-auto">
        {tabs.map((tab) => {
          const isActive = pathname === tab.href;
          const isRecord = tab.id === "record";

          return (
            <Link
              key={tab.id}
              href={tab.href}
              className={cn(
                "flex flex-col items-center justify-center gap-0.5 w-16 h-full transition-colors duration-200",
                isActive
                  ? "text-[var(--brand-primary)]"
                  : "text-[var(--text-muted)] hover:text-[var(--text-secondary)]"
              )}
            >
              {isRecord ? (
                /* Center record tab gets a branded circle */
                <span
                  className={cn(
                    "flex items-center justify-center h-12 w-12 rounded-full -mt-4 transition-all duration-200",
                    isActive
                      ? "bg-[var(--brand-primary)] text-[var(--bg-primary)] glow-brand"
                      : "bg-[var(--bg-elevated)] text-[var(--text-secondary)]"
                  )}
                >
                  {tab.icon}
                </span>
              ) : (
                tab.icon
              )}
              <span className="text-[10px] font-medium">{tab.label}</span>
            </Link>
          );
        })}
      </div>
    </nav>
  );
}
